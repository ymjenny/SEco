import streamlit as st

st.title("모델 설명 (SEco 구성)")

st.markdown("""
## 📌 SEco란?  
  
올림픽공원 공연장(진동·소음)과 스포츠센터(운동강도·공기질·혼잡)를 하나의 ‘안전·환경 통합지수(SEco 지수(SafetyEco INDEX))’로 묶어 AI 기반으로 실시간 위험도와 환기·에너지 운영을 자동 예측하는 ESG 기반 시스템입니다.

---

## 🧑‍💻 사용한 데이터
- 한국체육산업개발 KSPC 공연장 정보
- 스포츠센터 공기질/CO₂ 인원 증가량 연구값
- 서울시 미세먼지/계절 요인 데이터
- 관람객·장르별 위험 가중치 부여

---

## 💻 사용된 모델 : XGBoost Regressor
- OneHotEncoder + ColumnTransformer 전처리
- 관람 인원 / 장르 / 공연장 / MONTH 을 입력
- 위험도(0~100)을 출력

---

## 📢 위험등급 산정 기준
| 위험도 | 등급 |
|--------|------|
| 81~100 | 🚨 매우 위험 |
| 61~80 | ⚡ 경계 |
| 41~60 | ⚠️ 주의 |
| 21~40 | 🌿 양호 |
| 0~20 | 🌳 안전 |

""")
